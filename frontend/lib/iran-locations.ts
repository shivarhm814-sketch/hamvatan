export function findProvinceByCity(city?: string | null): string | undefined {
  if (!city) return undefined;
  return IRAN_PROVINCES.find((province) => province.cities.includes(city))?.name;
}

export function citiesOfProvince(province?: string | null): string[] {
  if (!province) return [];
  return IRAN_PROVINCES.find((p) => p.name === province)?.cities ?? [];
}
